import UIKit

extension UIColor {
    
    static var greenColor: UIColor {
        return UIColor(named: "Green") ?? .systemGreen
    }
    
    static var yellowColor: UIColor {
        return UIColor(named: "Yellow") ?? .systemYellow
    }
}
